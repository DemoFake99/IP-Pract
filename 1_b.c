// Write a program to declare some variables of type int, float and double. Assign some values to these variables and display these values

#include<stdio.h>
int main()
{
    int a = 10;
    float b = 20.5;
    double c = 30.5;
    printf("The value of a is %d",a);
    printf("The value of b is %f",b);
    printf("The value of c is %lf",c);
    return 0;
}
